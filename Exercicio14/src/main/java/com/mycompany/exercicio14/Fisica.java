public abstract class Fisica extends Pessoa{
    
    protected Sexo sexo;
    protected EstadoCivil estadoCivil;
    protected String dataDeNascimento;

    public Fisica(Sexo sexo, EstadoCivil estadoCivil, String dataDeNascimento, String nome, String telefone, String email, Endereco endereco) {
        super(nome, telefone, email, endereco);
        this.sexo = sexo;
        this.estadoCivil = estadoCivil;
        this.dataDeNascimento = dataDeNascimento;
    }

    public Sexo getSexo() {
        return sexo;
    }

    public void setSexo(Sexo sexo) {
        this.sexo = sexo;
    }

    public EstadoCivil getEstadoCivil() {
        return estadoCivil;
    }

    public void setEstadoCivil(EstadoCivil estadoCivil) {
        this.estadoCivil = estadoCivil;
    }

    public String getDataDeNascimento() {
        return dataDeNascimento;
    }

    public void setDataDeNascimento(String dataDeNascimento) {
        this.dataDeNascimento = dataDeNascimento;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Endereco getEndereco() {
        return endereco;
    }

    public void setEndereco(Endereco endereco) {
        this.endereco = endereco;
    }

    @Override
    public String toString() {
        
        return "\n=== DADOS DA PESSOA FÍSICA ===" +
                "Nome: " + super.nome +
                "Telefone: " + super.telefone +
                "E-mail: " + super.email +
                "Endereço: " + super.endereco +
                "Sexo: " + sexo +
                "Estado Cívil: " + estadoCivil +
                "Data de nascimento: "+ dataDeNascimento;
        
    }
    
    
}
